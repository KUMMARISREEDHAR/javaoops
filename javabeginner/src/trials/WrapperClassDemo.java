package trials;

public class WrapperClassDemo {
     public static void main(String[] rgs)
     {
    	 System.out.println(kavi(55)+"  "+method(55));
     }
    static Integer kavi(int k)
     {   
    	 Integer s=k;
    	 return s;
     }
    static int method(Integer m)
    {
    	int k=m;
    	return k;
    }
}
